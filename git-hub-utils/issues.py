# Создание или обновление issues
for issue in issues:
    title = issue["title"]
    body = issue["body"]

    if title in existing_issues:
        print(f"✏️ Обновляю issue: {title}")
        subprocess.run([
            "gh", "issue", "edit", str(existing_issues[title]),
            "--title", title,
            "--body", body
        ])
    else:
        print(f"🆕 Создаю issue: {title}")
        subprocess.run([
            "gh", "issue", "create",
            "--title", title,
            "--body", body
        ])